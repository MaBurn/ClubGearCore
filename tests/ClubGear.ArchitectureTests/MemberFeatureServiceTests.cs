    [Fact]
    public async Task UpdateAsync_MemberReferenceCreatingGrandchild_ThrowsValidationException()
    {
        using var connection = new SqliteConnection("Filename=:memory:");
        await connection.OpenAsync();

        var options = new DbContextOptionsBuilder<ApplicationDbContext>()
            .UseSqlite(connection)
            .Options;

        await using var db = new ApplicationDbContext(options);
        await db.Database.EnsureCreatedAsync();

        var firmaType = new MembershipType { Key = "Firma", Name = "Firma", IsActive = true, AllowsSubMembers = true, SubMemberLabel = "Mitarbeiter" };
        db.MembershipTypes.Add(firmaType);
        await db.SaveChangesAsync();

        var refField = new MembershipTypeField { MembershipTypeId = firmaType.Id, Key = "main_member", Label = "Hauptmitglied", FieldType = MemberMetadataFieldType.MemberReference };
        db.MembershipTypeFields.Add(refField);
        await db.SaveChangesAsync();

        var parent = new Member { MemberNumber = "P-1", FirstName = "Firma", LastName = "AG", IsActive = true, JoinedAt = DateTime.UtcNow, MembershipTypeId = firmaType.Id };
        var child = new Member { MemberNumber = "C-1", FirstName = "Kind", LastName = "Mueller", IsActive = true, JoinedAt = DateTime.UtcNow, MembershipTypeId = firmaType.Id };
        var outsider = new Member { MemberNumber = "X-1", FirstName = "Aussen", LastName = "Stehend", IsActive = true, JoinedAt = DateTime.UtcNow, MembershipTypeId = firmaType.Id };
        db.Members.AddRange(parent, child, outsider);
        await db.SaveChangesAsync();

        // child references parent => child is an existing sub-member, parent is an existing parent.
        db.MemberMetadataValues.Add(new MemberMetadataValue { MemberId = child.Id, FieldId = refField.Id, Value = parent.Id.ToString() });
        await db.SaveChangesAsync();

        var service = new MemberFeatureService(db, new NoopAuditLogService(), new MemberMetadataService());

        // outsider tries to reference the child, which is itself a sub-member => grandchild.
        var updated = new Member
        {
            Id = outsider.Id,
            MemberNumber = outsider.MemberNumber,
            FirstName = outsider.FirstName,
            LastName = outsider.LastName,
            IsActive = true,
            JoinedAt = outsider.JoinedAt,
            MembershipTypeId = firmaType.Id,
            MetadataInputs = new Dictionary<string, string?> { ["main_member"] = child.Id.ToString() }
        };

        var ex = await Assert.ThrowsAsync<ClubGear.Services.ValidationException>(() => service.UpdateAsync(updated, "tester"));
        Assert.Contains("das selbst ein Untermitglied ist", ex.Message);

        // The stored sub-member link for the child must be untouched.
        var childValue = await db.MemberMetadataValues.AsNoTracking().SingleAsync(v => v.MemberId == child.Id);
        Assert.Equal(parent.Id.ToString(), childValue.Value);
    }

