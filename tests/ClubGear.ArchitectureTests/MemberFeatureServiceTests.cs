    [Fact]
    public async Task CreateAsync_WithMembershipTypeAndMetadata_PersistsMetadataValuesAndAuditsThem()
    {
        using var connection = new SqliteConnection("Filename=:memory:");
        await connection.OpenAsync();

        var options = new DbContextOptionsBuilder<ApplicationDbContext>()
            .UseSqlite(connection)
            .Options;

        await using var db = new ApplicationDbContext(options);
        await db.Database.EnsureCreatedAsync();

        var vereinType = new MembershipType { Key = "Verein", Name = "Verein", IsActive = true };
        db.MembershipTypes.Add(vereinType);
        await db.SaveChangesAsync();

        var clubNameField = new MembershipTypeField
        {
            MembershipTypeId = vereinType.Id,
            Key = "club_name",
            Label = "Vereinsname",
            FieldType = MemberMetadataFieldType.Text,
            IsRequired = true
        };
        db.MembershipTypeFields.Add(clubNameField);
        await db.SaveChangesAsync();

        var auditLogService = new CapturingAuditLogService();
        var service = new MemberFeatureService(db, auditLogService, new MemberMetadataService());

        var member = new Member
        {
            FirstName = "Verein",
            LastName = "Beispiel",
            IsActive = true,
            JoinedAt = DateTime.UtcNow,
            MembershipTypeId = vereinType.Id,
            MetadataInputs = new Dictionary<string, string?> { ["club_name"] = "Modellbauverein e.V." }
        };

        await service.CreateAsync(member, "tester");

        var persistedValue = await db.MemberMetadataValues.AsNoTracking().SingleAsync(v => v.MemberId == member.Id);
        Assert.Equal(clubNameField.Id, persistedValue.FieldId);
        Assert.Equal("Modellbauverein e.V.", persistedValue.Value);

        Assert.Equal("Members.Create", auditLogService.LastAction);
        Assert.Null(auditLogService.LastBefore);
        var afterSnapshot = Assert.IsType<Member>(auditLogService.LastAfter);
        Assert.NotNull(afterSnapshot.MembershipType);
        Assert.Equal("Verein", afterSnapshot.MembershipType!.Key);
        Assert.Single(afterSnapshot.MetadataValues);
        Assert.Equal("Modellbauverein e.V.", afterSnapshot.MetadataValues[0].Value);
    }

    [Fact]
    public async Task UpdateAsync_ChangingMembershipType_RemovesStaleMetadataAndPersistsNewValues()
    {
        using var connection = new SqliteConnection("Filename=:memory:");
        await connection.OpenAsync();

        var options = new DbContextOptionsBuilder<ApplicationDbContext>()
            .UseSqlite(connection)
            .Options;

        await using var db = new ApplicationDbContext(options);
        await db.Database.EnsureCreatedAsync();

        var vereinType = new MembershipType { Key = "Verein", Name = "Verein", IsActive = true };
        var firmaType = new MembershipType { Key = "Firma", Name = "Firma", IsActive = true };
        db.MembershipTypes.AddRange(vereinType, firmaType);
        await db.SaveChangesAsync();

        var clubNameField = new MembershipTypeField { MembershipTypeId = vereinType.Id, Key = "club_name", Label = "Vereinsname", FieldType = MemberMetadataFieldType.Text };
        var companyNameField = new MembershipTypeField { MembershipTypeId = firmaType.Id, Key = "company_name", Label = "Firmenname", FieldType = MemberMetadataFieldType.Text };
        db.MembershipTypeFields.AddRange(clubNameField, companyNameField);
        await db.SaveChangesAsync();

        var member = new Member
        {
            MemberNumber = "M-2000",
            FirstName = "Wechsel",
            LastName = "Beispiel",
            IsActive = true,
            JoinedAt = DateTime.UtcNow,
            MembershipTypeId = vereinType.Id
        };
        db.Members.Add(member);
        await db.SaveChangesAsync();

        db.MemberMetadataValues.Add(new MemberMetadataValue { MemberId = member.Id, FieldId = clubNameField.Id, Value = "Alter Verein e.V." });
        await db.SaveChangesAsync();

        var auditLogService = new CapturingAuditLogService();
        var service = new MemberFeatureService(db, auditLogService, new MemberMetadataService());

        var updatedMember = new Member
        {
            Id = member.Id,
            MemberNumber = member.MemberNumber,
            FirstName = member.FirstName,
            LastName = member.LastName,
            IsActive = member.IsActive,
            JoinedAt = member.JoinedAt,
            MembershipTypeId = firmaType.Id,
            MetadataInputs = new Dictionary<string, string?> { ["company_name"] = "Neue Firma GmbH" }
        };

        var result = await service.UpdateAsync(updatedMember, "tester");

        Assert.Equal(MemberMutationStatus.Success, result);

        var remainingValues = await db.MemberMetadataValues.AsNoTracking().Where(v => v.MemberId == member.Id).ToListAsync();
        Assert.Single(remainingValues);
        Assert.Equal(companyNameField.Id, remainingValues[0].FieldId);
        Assert.Equal("Neue Firma GmbH", remainingValues[0].Value);

        var afterSnapshot = Assert.IsType<Member>(auditLogService.LastAfter);
        Assert.Equal("Firma", afterSnapshot.MembershipType?.Key);
        Assert.Single(afterSnapshot.MetadataValues);

        var beforeSnapshot = Assert.IsType<Member>(auditLogService.LastBefore);
        Assert.Equal("Verein", beforeSnapshot.MembershipType?.Key);
        Assert.Single(beforeSnapshot.MetadataValues);
        Assert.Equal("Alter Verein e.V.", beforeSnapshot.MetadataValues[0].Value);
    }

