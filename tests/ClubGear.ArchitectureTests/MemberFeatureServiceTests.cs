    private sealed class NoopAuditLogService : IAuditLogService
    {
        public Task LogAsync(AuditLogRecord record, CancellationToken cancellationToken = default)
        {
            return Task.CompletedTask;
        }

        public Task LogChangeAsync(
            string action,
            object? before,
            object? after,
            string? actor = null,
            string? source = null,
            string? targetType = null,
            string? targetId = null,
            object? metadata = null,
            CancellationToken cancellationToken = default)
        {
            return Task.CompletedTask;
        }
    }

    private sealed class CapturingAuditLogService : IAuditLogService
    {
        public string? LastAction { get; private set; }
        public object? LastBefore { get; private set; }
        public object? LastAfter { get; private set; }

        public Task LogAsync(AuditLogRecord record, CancellationToken cancellationToken = default)
        {
            LastAction = record.Action;
            LastBefore = record.Before;
            LastAfter = record.After;
            return Task.CompletedTask;
        }

        public Task LogChangeAsync(
            string action,
            object? before,
            object? after,
            string? actor = null,
            string? source = null,
            string? targetType = null,
            string? targetId = null,
            object? metadata = null,
            CancellationToken cancellationToken = default)
        {
            LastAction = action;
            LastBefore = before;
            LastAfter = after;
            return Task.CompletedTask;
        }
    }
